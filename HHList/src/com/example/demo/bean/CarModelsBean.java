package com.example.demo.bean;

/**
 * 
 * @Description 车型实体类
 * @author lsx
 */
public class CarModelsBean {

	private int ID;
	/** 品牌 */
	private String Brand;
	/** 全国最低价 */
	private String LowPrice;
	/** 指导价 */
	private String Guideprice;
	/** 发动机 */
	private String Engine;
	/** 变速箱 */
	private String SiftSpeed;
	/** 长*宽*高 */
	private String Volume;
	/** 最高车速 */
	private String UpSpeed;
	/** 官方加速 */
	private String AcceSpeed;
	/** 质保 */
	private String Quality;
	/** 长度 */
	private String Length;
	/** 高度 */
	private String Height;
	/** 宽度 */
	private String Width;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getLowPrice() {
		return LowPrice;
	}
	public void setLowPrice(String lowPrice) {
		LowPrice = lowPrice;
	}
	public String getGuideprice() {
		return Guideprice;
	}
	public void setGuideprice(String guideprice) {
		Guideprice = guideprice;
	}
	public String getEngine() {
		return Engine;
	}
	public void setEngine(String engine) {
		Engine = engine;
	}
	public String getSiftSpeed() {
		return SiftSpeed;
	}
	public void setSiftSpeed(String siftSpeed) {
		SiftSpeed = siftSpeed;
	}
	public String getVolume() {
		return Volume;
	}
	public void setVolume(String volume) {
		Volume = volume;
	}
	public String getUpSpeed() {
		return UpSpeed;
	}
	public void setUpSpeed(String upSpeed) {
		UpSpeed = upSpeed;
	}
	public String getQuality() {
		return Quality;
	}
	public void setQuality(String quality) {
		Quality = quality;
	}
	public String getLength() {
		return Length;
	}
	public void setLength(String length) {
		Length = length;
	}
	public String getHeight() {
		return Height;
	}
	public void setHeight(String height) {
		Height = height;
	}
	public String getWidth() {
		return Width;
	}
	public void setWidth(String width) {
		Width = width;
	}
	public String getAcceSpeed() {
		return AcceSpeed;
	}
	public void setAcceSpeed(String acceSpeed) {
		AcceSpeed = acceSpeed;
	}
	public String getBrand() {
		return Brand;
	}
	public void setBrand(String brand) {
		Brand = brand;
	}
}
